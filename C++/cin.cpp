#include <iostream>

using namespace std;

void main()
{
	int a, b, c;
	
	cout << "Birinci sayiyi giriniz : ";
	cin >> a;

	cout << "Ikinci sayiyi giriniz : ";
	cin >> b;

	cout << "Ucuncu sayiyi giriniz : ";
	cin >> c;

	cout << "Girdiginiz sayilarin toplamlari : " << a + b + c;


	cout << "\n\n------------------------------------------------- \n\n";


	int f, g, h;

	cout << "Sayilari giriniz : ";
	cin >> f >> g >> h;

	cout << "Girdiginiz sayilarin carpimlari : " << f * g * h << endl;
}

